COMMIT TRANSACTION GRANTE
END TRY
BEGIN CATCH
ROLLBACK TRANSACTION GRANTE;
DECLARE @MESSAGE NVARCHAR(200)= (SELECT ERROR_MESSAGE());
RAISERROR(@MESSAGE,18,1);
END CATCH

END
