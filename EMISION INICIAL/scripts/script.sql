CREATE TRIGGER [COM].[USUARIO_AU] ON [COM].[USUARIO] AFTER UPDATE
AS
BEGIN

DECLARE @USUARIO       NVARCHAR(20),
        @CLAVE         NVARCHAR(255),
        @CLAVE_BEFORE  NVARCHAR(255),
        @ADMIN         NVARCHAR(5),
        @ADMIN_BEFORE  NVARCHAR(5),
        @STRINGQUERY   NVARCHAR(500);


SELECT @USUARIO = USU_USU, @CLAVE = USU_CLAVE, @ADMIN = USU_ADMIN
FROM INSERTED;


SELECT @CLAVE_BEFORE = USU_CLAVE, --COMPRUEBA SI EL DATO ANTERIOR CAMBIO
       @ADMIN_BEFORE = USU_ADMIN  --COMPRUEBA SI EL DATO ANTERIOR ERA ADMINISTRADOR
FROM DELETED;


-- CAMBIA LA CLAVE DEL USUARIO DIRECTAMENTE EN LA BD.
IF(@CLAVE != @CLAVE_BEFORE)
BEGIN
   SELECT @STRINGQUERY = 'ALTER USER [' + @USUARIO + '] WITH PASSWORD = ''' + @CLAVE + ''';';
   EXEC SYS.SP_EXECUTESQL @STRINGQUERY;
END

ELSE
BEGIN
   IF(@ADMIN != @ADMIN_BEFORE)
   BEGIN
      IF(@ADMIN = 'TRUE')
      BEGIN
         SELECT @STRINGQUERY = 'GRANT ALTER ANY USER TO ['+ @USUARIO + '] WITH GRANT OPTION';
         EXEC SYS.SP_EXECUTESQL @STRINGQUERY;

         SELECT @STRINGQUERY = 'GRANT ALTER ANY ROLE TO ['+ @USUARIO + '] WITH GRANT OPTION';
         EXEC SYS.SP_EXECUTESQL @STRINGQUERY;

         SELECT @STRINGQUERY = 'ALTER ROLE [ADMIN_ROLE] ADD MEMBER ['+ @USUARIO + ']';
         EXEC SYS.SP_EXECUTESQL @STRINGQUERY;
      END
      ELSE
      BEGIN
         SELECT @STRINGQUERY = 'ALTER ROLE [ADMIN_ROLE] DROP MEMBER ['+ @USUARIO + ']';
         EXEC SYS.SP_EXECUTESQL @STRINGQUERY;
      END
   END
END

END
GO
ALTER TABLE [COM].[USUARIO] ENABLE TRIGGER [USUARIO_AU]
