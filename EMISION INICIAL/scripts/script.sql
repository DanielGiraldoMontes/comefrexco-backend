CREATE   PROCEDURE [COM].[DENY_ACCES]
@USER NVARCHAR(20),
@MODULE NVARCHAR(20),
@OPTION NVARCHAR(20)
AS
BEGIN

BEGIN TRY
BEGIN TRANSACTION GRANTE

BEGIN
DECLARE @STRINGQUERY NVARCHAR(500) = (SELECT 'DENY ' + @OPTION + ' ON [COM].[' + @MODULE + '] TO ['+ @USER + ']');
